    def load_character_map(self, char_map):
        """Loads a predefined character mapping for recognition."""
        self.char_dict = char_map

    def read_bmp_header(self, file_path):
        """Reads the BMP file header and extracts metadata."""
        with open(file_path, "rb") as f:
            header = f.read(54)
            offset = int.from_bytes(header[10:14], byteorder="little")
            width = int.from_bytes(header[18:22], byteorder="little")
            height = int.from_bytes(header[22:26], byteorder="little")
            bits_per_pixel = int.from_bytes(header[28:30], byteorder="little")

            if bits_per_pixel != 24:
                raise ValueError("Only 24-bit BMP files are supported")

            return offset, width, height

    def read_bmp_pixel_data(self, file_path, offset, width, height):
        """Reads pixel data from the BMP file."""
        with open(file_path, "rb") as f:
            f.seek(offset)
            row_padded = (width * 3 + 3) & ~3  # Rows are padded to multiples of 4 bytes
            data = []
            for _ in range(height):
                row = f.read(row_padded)[: width * 3]
                data.append(row)

            img = np.frombuffer(b"".join(data), dtype=np.uint8)
            img = img.reshape((height, width, 3))
            return img

    def isolate_red_pixels(self, img):
        """Isolates red pixels by creating a binary mask."""
        red_channel = img[..., 0]
        binary_img = (red_channel >= self.red_threshold).astype(np.uint8)
        return 1 - binary_img  # Invert colors for better segmentation

    def split_image(self, image):
        """Splits an image into equal vertical segments."""
        height, width = image.shape
        num_segments = width // self.col_width
        return [
            image[:, i * self.col_width : (i + 1) * self.col_width]
            for i in range(num_segments)
        ]

    def recognize_text(self, segments):
        """使用异或计算字符相似度来识别文本"""
        recognized = ""
        flatten_seg = [segment.flatten() for segment in segments]

        for each_letter in flatten_seg:
            similarity = []
            for char, pattern in self.char_dict.items():
                similarity.append((char, np.sum(each_letter ^ pattern)))

            most_similar_pair = min(similarity, key=lambda x: x[1])  # 找到最匹配的字符
            recognized += most_similar_pair[0]  # 取出匹配的字符

        return recognized

    def process_bmp(self, file_path):
        """Full pipeline: Read BMP, isolate red pixels, segment, and recognize text."""
        offset, width, height = self.read_bmp_header(file_path)
        rgb_img = self.read_bmp_pixel_data(file_path, offset, width, height)
        binary_img = self.isolate_red_pixels(rgb_img)
        segments = self.split_image(binary_img)
        return self.recognize_text(segments)
